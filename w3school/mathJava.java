public class mathJava{
    public static void main(String[] args){
        System.out.println(Math.max(5, 10));// max
        System.out.println(Math.min(5, 10));// min
        System.out.println(Math.sqrt(25)); // 5.0
        System.out.println(Math.abs(-25.5));// module
        System.out.println(Math.random()); // random number from 0 to 1
        System.out.println(Math.random() * 101);  // 0 to 100
    }
}